    <h2 class="h2-content" id="4.7 Caso de estudio">4.7 Caso de estudio</h2>
    <br>
    <p>
        Identificar el conjunto de elementos que intervienen en un sistema de compras online de una fábrica aplicando la TGS para finalmente proponer las actividades representadas en un diagrama de procesos.
    </p>
    <br>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Captura de pantalla (4).png" alt="Sistema de compras en línea">
    <br><br>
    <p>
        Puesto que el sistema de compras en línea no intercambia elementos de información con el medio ambiente que lo rodea se considera un sistema cerrado, de lo anterior se concluye:
    </p>
    <br>
    <ul>
        <li><strong>Se pueden inferir las siguiente relaciones:</strong></li><br>
        <li>Usuario solicitante contra el proceso de solicitud de compra en donde un usuario puede generar una o muchas solicitudes de compras.</li><br>
        <li>El almacén maneja el inventario de productos.</li><br>
        <li>La fábrica provee de muchos productos al almacén.</li><br>
        <li>El almacén promociona el producto para la solicitud.</li><br>
        <li>El usuario realiza el pago del producto.</li><br>
        <li>Contabilidad registra la factura de compra generada por la solicitud.</li><br>
    </ul>
    <br>
    <ul>
        <li><strong>Actores Responsables:</strong></li><br>
        <li>Usuario que genera la solicitud.</li><br>
        <li>Jefe de ventas.</li><br>
        <li>Administrador del sistema online.</li><br>
        <li>Jefe de contabilidad.</li><br>
        <li>Jefe del control de operaciones de fábrica.</li><br>
        <li>Jefe de inventarios.</li><br>
        <li>Empleados de la fábrica.</li><br>
        <li>Gerencia de área.</li><br>
    </ul>
