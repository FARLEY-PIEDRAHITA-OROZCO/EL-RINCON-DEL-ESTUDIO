@section('content')
    <h1 class="h1-content" id="Análisis de los procesos a nivel de negocio">4. Análisis de los procesos a nivel de negocio</h1>
    <br>
    <p>
        Con base en los conceptos de la teoría general de sistemas y partiendo de la visión del todo se debe comprender el funcionamiento real a partir de desestructurar ese todo en procesos, subprocesos, relaciones y actores que intervienen con el objetivo de identificar, comprender, evaluar y resolver problemas que ayudan a las instituciones a mejorar dichos procesos, reformar las opciones de negocio y brindar a sus consumidores a experimentar una mejor calidad de productos o servicios. Por esto, es necesario que realicen de manera constante, frecuente y repetitiva un análisis de sus procesos de negocio desde la TGS donde visualicen y comprendan cada uno de los pormenores que transforman el negocio con el objetivo de mejorar sus operaciones y el servicio al cliente. A continuación, una breve información al respecto:
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/tgs.gif" alt="tgs">
    <p>
        Una de las técnicas que permite conseguir estos objetivos es el análisis de procesos de negocio que significa llegar al conocimiento detallado de cada labor y que se hace abordando la operación central de valor hasta las que pueden considerarse como labores rama o periféricas no menos importantes. Ese es el nombre que se le da a la acción que realizan las organizaciones para revisar, documentar y entender sus procesos.
    </p>
    <br>
    <p>
        Tiene como objetivo el desglose de cada uno de los componentes en cada proceso como:
    </p>
    <br>
    <ul>
        <li>Entradas.</li>
        <li>Salidas.</li>
        <li>Procedimientos.</li>
        <li>Controles.</li>
        <li>Aplicaciones.</li>
        <li>Datos.</li>
    </ul>
    <br>
    <p>
        Es importante, comprender las acciones que integran cada uno de los siguientes elementos:
    </p>
    <br>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Acciones para el análisis.svg" alt="Acciones para el análisis">
    <br><br>
    <p>
        A continuación, se explica de manera detallada cada proceso:
    </p>
    <br>
    <h2 class="h2-content" id="4.1 Identificar los procesos">4.1 Identificar los procesos</h2>
    <br>
    <p>
        Para realizar una excelente identificación de procesos se recomienda realizar las siguientes actividades en dos escenarios:
    </p>
    <br>
    <p>
        <strong>1. Antes:</strong> se debe obtener acceso de información sobre la organización, para antes de llegar a esta, se investigue sobre el contexto de negocio en el que se mueve la empresa. Por ejemplo: determinar si se está hablando de un banco o una empresa de energía o una empresa que presta servicios y en ese caso qué tipo de servicios presta o por el contrario si es una empresa que fabrica productos, que productos fabrica es decir que hace. Conocer de ella también sus sedes (instalaciones físicas).
    </p>
    <br>
    <p>
        2. Cuando exista cercanía, acceso o contacto con la entidad, planear entonces la recolección de información aplicando las técnicas que existen para ello como lo es:
    </p>
    <br>
    <ul>
        <li>Método de la observación.</li>
        <li>La elaboración de formatos de encuestas.</li>
        <li>El enfoque de trabajo (focus group).</li>
        <li>Las entrevistas.</li>
    </ul>
    <br>
    <p>
        <em>Como resultado de estas técnicas se deben establecer claramente y como mínimo lo siguientes ítems: roles de los intervinientes, información de contactos con responsabilidades, misión, visión, objetivos estratégicos, estructura organizacional un organigrama de la entidad su distribución de dependencias u oficinas en donde se puede ubicar de manera ágil a los responsables que seguramente se convertirán en futuros miembros de los equipos de trabajo donde se presentará interacción (Sinergia laboral).</em>
    </p>
    <br>
    <p>
        Con lo anterior dispuesto, se puede dar el primer paso fundamental que corresponde a identificar, conocer, incluso ubicar los sitios en donde suceden en el mundo real, aquellos procesos que necesitan ser mejorados, descritos, analizados o propuestos para generar mejoras.
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Pasos a tener en cuenta.svg" alt="Pasos a tener en cuenta">
    <h2 class="h2-content" id="4.2 Determinar el equipo de trabajo">4.2 Determinar el equipo de trabajo</h2>
    <br>
    <p>
        Con base en la información recolectada y a la obtención de los requerimientos que permitieron identificar los procesos que necesitan mejorarse, se procede a formar el equipo de trabajo que los identificará, analizará y documentará. Lo ideal es que los responsables que conocen a detalle los procesos conformen el equipo.
        <br><br>
        Por lo tanto, el equipo debería estar integrado por el siguiente grupo humano:
    </p>
    <br>
    <ul>
        <li>El líder del proyecto o el proceso elegido.</li><br>
        <li>Un alto miembro de la organización que conoce los objetivos estratégicos.</li><br>
        <li>Los empleados vinculados al proceso.</li><br>
        <li>Los trabajadores de otras áreas que cuentan con la actual experiencia y que desean conocer las mejoras que se llevarán a cabo.</li>
    </ul>
    <br>
    <h2 class="h2-content" id="4.3 Generar los diagramas de los procesos de negocio">4.3 Generar los diagramas de los procesos de negocio</h2>
    <br>
    <p>
        Se trata de representar en una secuencia lógica los elementos expresados en la TGS, nombre del proceso el cual debe ser un verbo (fabricar, validar, informar) que permite modelar en un dibujo el proceso de manera rápida, dinámica y sencilla acerca de cómo fluyen las entradas en cada elemento y hacia dónde se deben dirigir sus salidas. En el diagrama es fundamental definir las competencias de cada uno de los integrantes del equipo en cada tarea.
    </p>
    <br>
    <p>
        El diagrama de procesos es también conocido como diagrama de flujo y en el mundo de los sistemas orientados por procesamiento electrónico de datos obedece a un algoritmo, es decir, al conjunto de pasos lógicos para resolver un problema, es una de las herramientas más útiles para cualquier entidad que quiere gestionar proyectos de todo tipo tanto a nivel interno como externo a ella. Permite abordar la descomposición y conocimiento de los procesos empresariales en orden lógico plasmados dentro de un único documento con sus relaciones, visualizar oportunidades de mejora y, en general para dar importancia y generar control hasta el más mínimo detalle a todos los procesos de una compañía, por pequeños que puedan considerarse. Encontrando con ello, lo siguiente:
    </p>
    <br>
    <h2 class="h2-content" id="a. Diagrama de procesos">a. Diagrama de procesos</h2>
    <br>
    <p>
        Teniendo en cuenta la teoría general de sistemas antes relacionada a un diagrama de procesos, es la forma en la cual se aplica el enfoque sistémico para representar de manera gráfica los componentes de las principales actividades que pueden inferirse a su vez como subsistemas que conforman el todo interoperación con orden lógico guiados por sus interrelaciones. Muestra el flujo secuencial de interacción de las actividades de un proceso a través de símbolos gráficos para proporcionar una mejor visualización del funcionamiento del mismo, ayudando a su entendimiento y haciendo su descripción más didáctica, visual e intuitiva.
    </p>
    <p>
        Así, el diagrama de procesos se convierte en una herramienta primordial para llevar a cabo el análisis de manera global y ver en qué acciones del flujo de trabajo se pueden introducir métricas, controles o descubrir oportunidades para mejorar, especialmente pretende orientar los aspectos que permiten como primera medida aumentar la productividad de los empleados, delimitar y asignar las competencias para cada tarea, ver cuellos de botella es decir posibles atascos en el devenir del flujo y en general para aclarar el propio flujo de trabajo.
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Tipode de diagramas de procesos.svg" alt="Tipos de diagramas de procesos">
    <p>
        En definitiva, es una de las herramientas que más ayuda en la consecución de una mejor gestión empresarial integral, especialmente en aquellas entidades en donde los procesos suelen ser acoplados, de alta complejidad, bastante rígidos o están muy interrelacionados.
    </p>
    <p>
        Teniendo en cuenta las labores a ejecutar vs un tiempo estimado o empleado los diagramas de procesos más utilizados para la gestión de proyectos son el diagrama de Gantt y el diagrama de Pert. Los dos diagramas permiten planificar, simplificar, programar actividades, asignar responsabilidades, pesos por valor de actividad de forma sencilla y muy visual que facilitan su entendimiento.
    </p>
    <br>
    <h2 class="h2-content" id="b. Elementos de un diagrama de procesos">b. Elementos de un diagrama de procesos</h2>
    <br>
    <p>
        A la hora de construir o representar y dibujar un diagrama de procesos, existe un lenguaje de escritura que se conforma por una serie de símbolos comúnmente aceptados que han sido establecidos por el estándar ANSI.
    </p>
    <br>
    <p>
        Estos diagramas están compuestos por elementos gráficos como lo son terminadores, símbolos de procesos, de subprocesos y de decisiones, líneas con flechas y conectores. Los principales elementos están representados de la siguientes formas:
    </p>
    <br>
    <p>
        <strong>1. Procesos o actividades:</strong><br>
        Su representación es un rectángulo, se refieren a una acción en un proceso de negocios. Son, posiblemente, los elementos más importantes de un diagrama en cada uno de estos se recomienda de manera detallada aplicar el análisis sistémico en cada actividad, son los que siempre deben aparecer, y por eso deben describirse de forma clara y concisa. Deben ser descritos con una frase única de verbo y sustantivo. Por ejemplo: “Redactar un nuevo informe para presentar a la gerencia de la empresa”.
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/proceso.svg" alt="Proceso">
    <p>
        <strong>2. Subprocesos:</strong><br>
        Estos se representan con un rectángulo y líneas dobles a cada lado. Deben ser considerados como partes de un proceso padre de mayor complejidad y sirven para determinar funciones más simples al diagrama de flujo incluso reutilizables.
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/subproceso.svg" alt="Subproceso">
    <p>
        <strong>3. Nodos de decisión:</strong><br>
        Son los que se representan mediante un triángulo doble (Diamante) y son los que muestran los puntos de decisión a seguir bien sea positivo si se responde a la decisión de “sí” o negativo “no”.
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Nodos de decisión.svg" alt="Nodos de decisión">
    <p>
        <strong>4. Conectores:</strong><br>
        Representados por círculos o cuadros conectores que se deben acompañar con la utilización de etiquetas o nombres de caracteres comúnmente alfanuméricos. Aseguran que todos los procesos están conectados de forma lógica y correcta para la correcta navegación entre páginas.
    </p>
    <br>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Conectores.svg" alt="Conectores">
    <p>
        <strong>5. Líneas de flecha:</strong><br>
        Utilizadas gráficamente para mantener el hilo conductor del flujo de trabajo la coherencia y claridad en un diagrama de procesos. Se pueden dibujar hacia todas las direcciones, normalmente representa el camino por el que fluirá el diagrama.
    </p>
    <br>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Líneas de flecha.svg" alt="Líneas de flecha">
    <br><br>
    <p>
        <strong>6. Conector para elementos fuera de página:</strong><br>
        Permite establecer relaciones para elementos que no están en la misma página.
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Conector para elementos fuera de página.svg" alt="Conector para elementos fuera de página">
    <br>
    <p>
        <strong>7. Entrada manual:</strong><br>
        Permite representar un acción la cual requiere intervención humana.
    </p>
    <br>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Entrada manual.svg" alt="Entrada manual">
    <br>
    <p>
        <strong>8. Almacenamiento:</strong><br>
        Representa el sitio en donde normalmente se aloja la información a manera de datos que comúnmente son de carácter estático.
    </p>
    <br>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Almacenamiento.svg" alt="Almacenamiento">
    <br>
    <p>
        <strong>9. Múltiples documentos:</strong><br>
        Representa la necesidad de tener en cuenta varios elementos de información o normas, especialmente son considerados como entradas.
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Múltiples documentos.svg" alt="Múltiples documentos">
    <p>
        <strong>10. Terminadores:</strong><br>
        Se representan por un rectángulo con esquinas curvas. Son muy importante se consideran como los mayores delimitadores del flujo y deben aparecer siempre al inicio y al final de un diagrama de flujo para indicar la parte del documento o gráfico en donde inicia y en donde finaliza un diagrama.
    </p>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/Terminadores.svg" alt="Terminadores">
    <h2 class="h2-content" id="c. Un buen diagrama de procesos">c. Un buen diagrama de procesos</h2>
    <br>
    <p>
        Al momento de crear un diagrama de procesos se recomienda guiarse por al menos cumplir los siguientes pasos:
    </p>
    <br>
    <p>
        <strong>1. Ubicar o según sea el caso abstraer los componentes del proceso:</strong> en este paso se aclaran cuáles son los parámetros de entrada al proceso y sus salidas, así como las actividades que se desarrollan en el mismo.
    </p>
    <br>
    <p>
        <strong>2. Ordenar de manera lógica las actividades:</strong> dado que el diagrama de procesos es un flujo, es necesario ordenar las actividades para saber cuál es su secuencia.
    </p>
    <br>
    <p>
        <strong>3.</strong> Elegir los símbolos adecuados conforme el tipo de tarea para que cualquier usuario pueda diferenciar los distintos elementos que forman el diagrama.
    </p>
    <br>
    <p>
        <strong>4.</strong> Hacer la conexión y relación entre las actividades, para lo cual se utilizan flechas y líneas de puntos o continuas.
    </p>
    <br>
    <p>
        <strong>5. </strong> Representar el comienzo y el final de cada proceso. Aunque pueda parecer trivial e irrelevante, lo cierto es que este es un punto muy importante para establecer los límites de cada proceso u actividad de cara a los propietarios de los procesos, gerentes y supervisores.
    </p>
    <br>
    <p>
        <strong>6.</strong> Validar, socializar con los integrantes del equipo y revisar el diagrama de procesos para asegurarse que su representación obedece a un gráfico acorde con lo que se quiere reflejar.
    </p>
    <br>
    <h2 class="h2-content" id="d. Dividendos de los diagramas de procesos">d. Dividendos de los diagramas de procesos</h2>
    <br>
    <p>
        Utilizar un diagrama de flujo le aporta los siguientes beneficios a tu empresa:
    </p>
    <br>
    <p>
        <strong>1.</strong> Permite obtener la primera aproximación desde el punto de vista algorítmico de cada actividad o proceso así como también puntos de interoperabilidad.
    </p>
    <br>
    <p>
        <strong>2.</strong> Favorece la comprensión del proceso que trata de simular. Dado que normalmente están representadas a través de algún elemento gráfico y sus nodos están interconectados entre sí, gracias a un diagrama de este tipo es más fácil saber cuáles son las tareas que se dibujan, así como sus relaciones de dependencia.
    </p>
    <br>
    <p>
        <strong>3. </strong> Permite identificar de una forma rápida los inconvenientes y dónde se producen. Así, es más fácil saber dónde se crean o se presentan posibles cuellos de botella, las responsabilidades de cada área y los puntos de decisión. Asimismo, permite analizar de una forma mucho más exhaustiva el problema, lo cual hace posible que aparezcan nuevas soluciones y alternativas.
    </p>
    <br>
    <p>
        <strong>4.</strong> Permite definir claramente las fronteras de cada proceso, ya que todas las tareas están lo suficientemente delimitadas, incluso aunque estén interrelacionadas. De esta manera, se puede saber qué tareas suponen un mayor valor añadido y cuáles no aportan tanto.
    </p>
    <br>
    <p>
        <strong>5.</strong> También es una buena herramienta de capacitación, instrucción y formación para nuevos empleados que se integran dentro de la empresa facilitando su proceso de adaptación a la organización.
    </p>
    <br>
    <img class="img-2 img-3" src="/images/caracterizacion-de-procesos/diagrama de procesos.svg" alt="Diagrama de procesos">
