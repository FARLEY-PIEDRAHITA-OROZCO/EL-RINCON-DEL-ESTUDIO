    <h1 class="h1-content" id="enfoque de sistemas">2. Enfoque de sistemas</h1>
    <br>
    <p>
        Un puntal básico unido a los respectivos análisis (simulación, teoría de comportamientos, teoría de colas, teoría de juegos) que se pueden aplicar para la resolución de problemas usando el método científico es el enfoque de sistemas:
    </p>
    <br>
    <p>
        "<em>Se trata de comprender el funcionamiento de una organización u objeto de estudio desde una perspectiva holística e integradora, en donde lo importante son las relaciones entre los componentes. Se llama holismo al punto de vista que se interesa más por el todo que por la suma de las partes. El enfoque sistémico no concibe la posibilidad de explicar un elemento si no es precisamente en su relación con el todo. Metodológicamente, por tanto, el enfoque sistémico es lo opuesto al individualismo metodológico, aunque esto no implique necesariamente que estén en contradicción.</em>"
    </p>
    <br>
    <p>
        (Centro Tic Junta de Andalucía, 2021).
